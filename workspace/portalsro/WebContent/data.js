function mostraData()
        {
                var data="";
                var d=new Date();
                data = "<h5>" + diaSemana(d.getDay()) + ",</h5><br/><h5>" + d.getDate() + " de " + pegaMes(d.getMonth()) + " de " + d.getFullYear() + "</h5>";
                return data;

        }
        function diaSemana(dia)
        {
                var arrDiaSemana = new Array();
                arrDiaSemana[0]="Domingo";
                arrDiaSemana[1]="Segunda-feira";
                arrDiaSemana[2]="Terça-feira";
                arrDiaSemana[3]="Quarta-feira";
                arrDiaSemana[4]="Quinta-feira";
                arrDiaSemana[5]="Sexta-feira";
                arrDiaSemana[6]="Sábado";
                return arrDiaSemana[dia];
        }
        function pegaMes(mes)
        {
                var arrMes = new Array();
                arrMes[0] = "Janeiro";
                arrMes[1] = "Fevereiro";
                arrMes[2] = "Março";
                arrMes[3] = "Abril";
                arrMes[4] = "Maio";
                arrMes[5] = "Junho";
                arrMes[6] = "Julho";
                arrMes[7] = "Agosto";
                arrMes[8] = "Setembro";
                arrMes[9] = "Outubro";
                arrMes[10] = "Novembro";
                arrMes[11] = "Dezembro";
                return arrMes[mes];
        }

