package br.com.correios.sromonitor.vos;

import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author Jean Clay Souza da Silva - Analista - DESUN/DSGL/CRO
 */
public class ObjetoIrregular implements Serializable {
    
    private String etiqueta;
    private String evento;
    private Date dataCriacao;
    private String unidade;
    private String codigoUnidade;
    
    /** Creates a new instance of ObjetoIrregular */
    public ObjetoIrregular() {
    }

    public String getEtiqueta() {
        return etiqueta;
    }

    public void setEtiqueta(String etiqueta) {
        this.etiqueta = etiqueta;
    }

    public String getEvento() {
        return evento;
    }

    public void setEvento(String evento) {
        this.evento = evento;
    }

    public Date getDataCriacao() {
        return dataCriacao;
    }

    public void setDataCriacao(Date dataCriacao) {
        this.dataCriacao = dataCriacao;
    }

    public String getUnidade() {
        return unidade;
    }

    public void setUnidade(String unidade) {
        this.unidade = unidade;
    }

    public String getCodigoUnidade() {
        return codigoUnidade;
    }

    public void setCodigoUnidade(String codigoUnidade) {
        this.codigoUnidade = codigoUnidade;
    }
    
}
